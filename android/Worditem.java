import android.graphics.Color;

public class Worditem {
    public class WordItem {
        private String text;
        private float x, y;
        private Color color;
        private boolean visible;

        public WordItem(String text, float x, float y) {
            this.text = text;
            this.x = x;
            this.y = y;
            this.color = color;
            this.visible = true;
        }
    }
}
